#include "../_启动窗口.h"

void __启动窗口::载入(窗口* 父窗, bool 模态)
{
	if (窗口句柄)return;
	窗口::创建({});
	
	超级列表框::参数 cs;
	cs.左边 = 10;
	cs.顶边 = 10;
	cs.宽度 = 350;
	cs.高度 = 200;
	cs.类型 = 超级列表框类型::报表列表框;
	cs.显示表格线 = true;
	cs.整行选择 = true;
	cs.边框 = 超级列表框边框::凹入式;
	cs.文本颜色 = 颜色::黑色;
	//cs.背景颜色 = 颜色::透明;
	cs.是否有检查框 = true;
	超列.创建(cs, this);
	
	窗口::完毕(模态);
}