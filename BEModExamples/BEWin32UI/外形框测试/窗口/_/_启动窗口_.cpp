#include "../_启动窗口.h"

void __启动窗口::载入(窗口* 父窗, bool 模态)
{
	if (窗口句柄)return;

	参数 cs;
	cs.宽度 = 600;
	cs.高度 = 400;
	窗口::创建(cs, 父窗, 模态);

	// 测试 1：圆角矩形
	外形框::参数 fcs1(20, 20, 200, 100);
	fcs1.外形 = 外形式样::圆角矩形;
	fcs1.线条颜色 = 颜色::红色;
	fcs1.圆角半径 = 20;
	外形框_测试.创建(fcs1, this);

	// 测试 2：凹入式 (系统风格)
	外形框::参数 fcs2(240, 20, 200, 100);
	fcs2.外形 = 外形式样::凹入式;
	外形框* frame2 = new 外形框();
	frame2->创建(fcs2, this);

	// 测试 3：椭圆
	外形框::参数 fcs3(20, 140, 200, 100);
	fcs3.外形 = 外形式样::椭圆;
	fcs3.背景颜色 = RGB(220, 240, 255);
	外形框* frame3 = new 外形框();
	frame3->创建(fcs3, this);

	// 测试 4：凸出式 (系统风格)
	外形框::参数 fcs4(240, 140, 200, 100);
	fcs4.外形 = 外形式样::凸出式;
	外形框* frame4 = new 外形框();
	frame4->创建(fcs4, this);

	__启动窗口::完毕(模态);
}