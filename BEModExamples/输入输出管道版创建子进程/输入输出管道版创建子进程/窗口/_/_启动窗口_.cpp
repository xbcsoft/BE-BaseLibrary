#include "../_启动窗口.h"

void __启动窗口::载入(窗口* 父窗, bool 模态)
{
	if (窗口句柄)return;
	
	窗口::参数 cs;
	cs.宽度 = 400;
	cs.高度 = 200;
	cs.标题 = L"主窗口 - MD5计算结果获取";
	cs.边框 = 窗口边框::普通固定边框;
	窗口::创建(cs);
	
	__启动窗口::完毕(模态);
}

void __启动窗口::完毕(bool 模态) {
	打开计算窗口按钮.创建(按钮::参数(20, 20, 120, 35), this);
	打开计算窗口按钮.标题_(L"弹出计算窗口");

	结果编辑框.创建(编辑框::参数(20, 70, 340, 35), this);
	结果编辑框.内容_(L"等待计算结果...");
	结果编辑框.输入方式_(编辑框输入方式::只读方式);

	窗口::完毕(模态);
}