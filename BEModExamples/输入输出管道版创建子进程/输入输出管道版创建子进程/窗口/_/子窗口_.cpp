#include "../子窗口.h"

void _子窗口::载入(窗口* 父窗, bool 模态)
{
	if (窗口句柄)return;
	
	窗口::参数 cs;
	cs.宽度 = 350;
	cs.高度 = 150;
	cs.标题 = L"计算 MD5";
	子窗口.创建(cs, 父窗, 模态);
	
	_子窗口::完毕(模态);
}

void _子窗口::完毕(bool 模态) {
	输入编辑框.创建(编辑框::参数(20, 20, 290, 30), this);
	输入编辑框.内容_(L"123456");

	计算按钮.创建(按钮::参数(115, 70, 100, 35), this);
	计算按钮.标题_(L"开始计算");

	窗口::完毕(模态);
}
