#include "stdafx.h"

void main()
{
	be::print("简单输出现行时间与时间戳", 取现行时间(), 取现行时间().取时间戳());

	// 1. 支持完整的日期时间字面量链式调用 (2026_y/3/11 /18/30/59)
	// 最后加 + 8 表示这是东八区时间
	日期时间型 dt = 2026_y/3/11 /18/30/59 + 8;
	be::print("完整字面量构造 (东八区):", dt); //输出部分不含时区

	// 2. 也支持仅日期（时间部分默认为 00:00:00，默认时区为全局设定）
	日期时间型 ymd = 2026_y/5/20; //也可再填尾部+整数时区
	be::print("仅日期:", ymd, "\n");

	// 3. 原有的文本解析方式
	日期时间型 time = 到时间("2026-03-11 01:23:59");
	time.毫秒 = 10; // 支持再额外设置毫秒
	be::print("含设置毫秒的输出", time);
	be::print("基本组件输出", sprintF("%04u-%02u-%02u %02u:%02u:%02u",
		time.年, time.月, time.日, time.时, time.分, time.秒, "\n"
	));
	be::print("对日期时间加22天", 增减时间(time, 期::日, 22), "\n");

	// 4. 时间戳操作（一进一出/一加一减刚好抵消）
	int64 a = time.取时间戳(); //毫秒部分会忽略
	be::print(time, "从时间戳再构造日期时间", a, 日期时间型(a));

	// 5. 时区切换
	期::全局时区 = 8; // 东八区
	日期时间型 now = 取现行时间();
	be::print("JS时间格式 (GMT+8):", 到格式化JS时间(now));

	日期时间型 diy = 取现行时间(0); // UTC
	be::print("JS时间格式 (GMT/UTC):",到格式化JS时间(diy));
}