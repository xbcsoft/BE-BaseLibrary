#include "../窗口1.h"

void _窗口1::载入(窗口* 父窗, bool 模态)
{
	if (窗口句柄)return;
	窗口1.创建({ 0, 0, 500, 360, true }, 父窗, 模态);
	标题_(L"树型框测试窗口");
	{
		树型框::参数 cs(10, 10, 300, 300);
		cs.是否有检查框 = true;
		树型框1.创建(cs, this);
	}
	{
		按钮::参数 cs(320, 10, 80, 30);
		cs.标题 = L"导出";
		导出按钮.创建(cs, this);

		cs.左边 = 320;
		cs.顶边 = 50;
		cs.标题 = L"载入";
		载入按钮.创建(cs, this);
	}

	窗口::完毕(模态);
}
