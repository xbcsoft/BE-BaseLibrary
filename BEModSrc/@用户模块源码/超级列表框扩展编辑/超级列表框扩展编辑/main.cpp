#include "stdafx.h"
#include <BEWin32UI/runtime.h>
#include "超级列表框扩展编辑.h"

struct 测试窗口 : 窗口
{
	超级列表框 列表框;

	void 载入(窗口* 父窗 = 0, bool 模态 = 0)
	{
		// 1. 创建超级列表框
		超级列表框::参数 cs;
		cs.左边 = 10;
		cs.顶边 = 10;
		cs.宽度 = 400;
		cs.高度 = 300;
		cs.整行选择 = true;
		cs.显示表格线 = true;

		列表框.创建(cs, this);

		// 2. 插入列
		列表框.插入列(0, L"姓名", 100);
		列表框.插入列(1, L"年龄", 80);
		列表框.插入列(2, L"职业", 120);

		// 3. 插入表项
		列表框.插入表项(0, L"张三");
		列表框.置标题(0, 1, L"25");
		列表框.置标题(0, 2, L"程序员");

		列表框.插入表项(1, L"李四");
		列表框.置标题(1, 1, L"30");
		列表框.置标题(1, 2, L"设计师");

		列表框.插入表项(2, L"王五");
		列表框.置标题(2, 1, L"28");
		列表框.置标题(2, 2, L"产品经理");

		// 4. 启用双击扩展编辑
		超级列表框_置扩展编辑(列表框.窗口句柄, [](HWND hwnd, int r, int c) -> bool {
			be::print("准备编辑表项：", r, "行", c, "列");
			return true; // 返回 true 允许编辑
		});
	}
}win;

void main()
{
	全局初始化配置(GetModuleHandleW(NULL), true);

	窗口::参数 cs;
	cs.标题 = L"超级列表框扩展编辑测试";
	cs.宽度 = 450;
	cs.高度 = 360;
	win.创建(cs);
	win.载入();
	Win32消息循环();
}